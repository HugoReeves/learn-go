package main

import "fmt"

func main() {
	//Strings
	fmt.Println("Hello world") //Hello world
	fmt.Println("Go", "lang")  //Go lang

	//Ints
	fmt.Println(7)     //7
	fmt.Println(7 + 7) //14

	//Floats
	fmt.Println(0.7777 + 0.7777) //1.554
	fmt.Println(0.5 * 2)         //1

	//Bools
	fmt.Println(true) //true

	//Issues with values and types
	fmt.Println("7" + "7") //77
}
